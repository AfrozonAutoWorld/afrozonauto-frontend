/** Normalized token for matching OEM / UI labels (hyphens, spaces stripped). */
export function normalizeCarMakeKey(s: string): string {
  return s.toLowerCase().replace(/[^a-z0-9]/g, '');
}

function sanitizeModelNames(arr: string[] | undefined): string[] {
  return arr?.map((m) => m.trim()).filter((m) => m.length > 0) ?? [];
}

/**
 * When normalized UI/API tokens do not match `carModels` keys (e.g. "mercedes" vs MercedesBenz).
 */
const CATALOG_KEY_BY_NORMALIZED_TOKEN: Partial<Record<string, keyof typeof carModels>> = {
  mercedes: 'MercedesBenz',
};

/**
 * Auto.dev often uses shorter OEM strings than our VEHICLE_MAKES labels. Try these when
 * normalizing "Mercedes-Benz" (mercedesbenz) against API keys like "Mercedes" (mercedes).
 */
const API_MAKE_KEY_GROUPS: Record<string, readonly string[]> = {
  mercedesbenz: [
    'Mercedes-Benz',
    'Mercedes Benz',
    'Mercedes',
    'MercedesBenz',
    'Mercedes‐Benz', // unicode hyphen
  ],
  landrover: ['Land Rover', 'LandRover', 'LAND ROVER'],
  alfaromeo: ['Alfa Romeo', 'AlfaRomeo'],
  astonmartin: ['Aston Martin', 'AstonMartin'],
  rollsroyce: ['Rolls-Royce', 'Rolls Royce', 'RollsRoyce'],
};

/**
 * Maps UI / API make names (e.g. Mercedes-Benz, Land Rover) to keys on `carModels`
 * (e.g. MercedesBenz, LandRover).
 */
export function resolveCarModelsCatalogKey(make: string): keyof typeof carModels | undefined {
  if (!make) return undefined;
  const keys = Object.keys(carModels) as (keyof typeof carModels)[];
  if (
    Object.hasOwn(carModels, make) &&
    carModels[make as keyof typeof carModels]?.length
  ) {
    return make as keyof typeof carModels;
  }
  const t = normalizeCarMakeKey(make);
  const hit = keys.find((k) => normalizeCarMakeKey(String(k)) === t);
  if (hit && carModels[hit]?.length) return hit;
  const alias = CATALOG_KEY_BY_NORMALIZED_TOKEN[t];
  return alias && carModels[alias]?.length ? alias : undefined;
}

/**
 * Resolves which key in `makeModels` (Auto.dev reference) matches a UI make label.
 */
export function resolveMakeModelsApiKey(
  uiMake: string,
  makeModels: Record<string, string[]>
): string | undefined {
  const mapKeys = Object.keys(makeModels);
  const nonEmpty = (k: string) => sanitizeModelNames(makeModels[k]).length > 0;

  if (uiMake && Array.isArray(makeModels[uiMake]) && nonEmpty(uiMake)) {
    return uiMake;
  }

  const n = normalizeCarMakeKey(uiMake);
  const byNorm = mapKeys.find((k) => normalizeCarMakeKey(k) === n);
  if (byNorm && nonEmpty(byNorm)) return byNorm;

  const group = API_MAKE_KEY_GROUPS[n];
  if (group) {
    for (const cand of group) {
      if (makeModels[cand] !== undefined && nonEmpty(cand)) return cand;
    }
    for (const k of mapKeys) {
      if (!nonEmpty(k)) continue;
      if (group.some((c) => normalizeCarMakeKey(c) === normalizeCarMakeKey(k))) return k;
    }
  }

  return undefined;
}

/**
 * Model names for filters: prefer non-empty Auto.dev list, else static catalog (sorted).
 */
export function getModelNamesForMake(uiMake: string, makeModels: Record<string, string[]>): string[] {
  const ui = (uiMake ?? '').trim();
  if (!ui) return [];

  const apiKey = resolveMakeModelsApiKey(ui, makeModels);
  if (apiKey) {
    const list = sanitizeModelNames(makeModels[apiKey]);
    if (list.length) return [...list].sort((a, b) => a.localeCompare(b));
  }
  let ck = resolveCarModelsCatalogKey(ui);
  if (ck && carModels[ck]?.length) {
    return [...carModels[ck]].sort((a, b) => a.localeCompare(b));
  }
  if (apiKey) {
    ck = resolveCarModelsCatalogKey(apiKey);
    if (ck && carModels[ck]?.length) {
      return [...carModels[ck]].sort((a, b) => a.localeCompare(b));
    }
  }
  return [];
}
